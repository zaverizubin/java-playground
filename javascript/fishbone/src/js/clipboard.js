function Clipboard(){
    this.styles;

    this.settings;

    this.getStyles = function(){
        return this.styles;
    };

    this.setStyles = function(styles){
        this.styles = styles;
    };

    this.getSettings = function(){
        return this.settings;
    };

    this.setSettings = function(settings){
        this.settings = settings;
    };

}

